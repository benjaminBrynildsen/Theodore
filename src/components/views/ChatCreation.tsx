function getOrCreateChatSessionId(): string {
  try {
    const existing = localStorage.getItem(CHAT_SESSION_ID_KEY);
    if (existing) return existing;
  } catch {
    // localStorage not available — fall through to a fresh id.
  }
  const fresh = (typeof crypto !== 'undefined' && 'randomUUID' in crypto)
    ? crypto.randomUUID()
    : `session-${Date.now()}-${Math.random().toString(36).slice(2, 10)}`;
  try { localStorage.setItem(CHAT_SESSION_ID_KEY, fresh); } catch {}
  return fresh;
}

function formatFileSize(bytes: number): string {
  if (bytes < 1024) return `${bytes} B`;
  if (bytes < 1024 * 1024) return `${(bytes / 1024).toFixed(1)} KB`;
  return `${(bytes / (1024 * 1024)).toFixed(1)} MB`;
}

function messagePromptText(m: Message): string {
  if (!m.attachments || m.attachments.length === 0) return m.content;
  const attachBlock = m.attachments
    .map((a) => `[Attached file: ${a.fileName}${a.truncated ? ' (truncated)' : ''}]\n${a.text}`)
    .join('\n\n');
  return m.content ? `${m.content}\n\n${attachBlock}` : attachBlock;
}

